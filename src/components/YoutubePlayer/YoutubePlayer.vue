<template>
    <div
        ref="videoWrap"
        class="vs-video-wrap"
        :class="{'vs-picture-in-picture': isPipEnabled}"
    >
        <div class="vs-widescreen">
            <div
                ref="youtubeIframe"
                class="vs-iframe"
            ></div>
        </div>
    </div>
</template>

<script>
import PlayerUtils from "../VideoPlayer/player-utils";

export default {
    name: 'YoutubePlayer',
    props: {
        videoId: {
            type: [String, Number],
            default: () => null,
        },

        currentSecond: {
            type: Number,
            default: () => 0,
        },

        totalDuration: {
            type: Number,
            default: () => 0,
        },

        progressState: {
            type: String,
            default: () => 'unstarted',
        },

        contentId: {
            type: [String, Number],
            default: () => null,
        },

        useIntersectionObserver: {
            type: Boolean,
            default: () => false,
        },
    },
    data() {
        return {
            player: null,
            syncInterval: null,
            currentTime: 0,
            isPipEnabled: false,
        };
    },
    computed: {
        isMobileViewport: {
            cache: false,
            get() {
                return window.matchMedia('(min-width: 641px)').matches === false;
            },
        },
    },
    mounted() {
        const youtubeIframeApi = document.getElementById('youtubeIframeApi');

        if (youtubeIframeApi == null) {
            this.appendIframeApi();

            window.onYouTubeIframeAPIReady = () => {
                this.initPlayer();
            };
        } else {
            this.initPlayer();
        }

        if (this.useIntersectionObserver) {
            this.enableIntersectionObserver();
        }
    },
    beforeDestroy() {
        clearInterval(this.syncInterval);
    },
    methods: {

        appendIframeApi() {
            const scriptTag = document.createElement('script');
            const firstScriptTag = document.querySelector('script');

            scriptTag.setAttribute('id', 'youtubeIframeApi');
            scriptTag.setAttribute('src', 'https://www.youtube.com/iframe_api');

            firstScriptTag.parentNode.insertBefore(scriptTag, firstScriptTag);
        },

        initPlayer() {
            const { youtubeIframe } = this.$refs;
            const urlParams = new URLSearchParams(window.location.search);
            const timeToSeekTo = urlParams.get('time') || this.currentSecond;
            const vm = this;

            this.player = new YT.Player(youtubeIframe, {
                videoId: this.videoId,
                playerVars: {
                    modestbranding: 1,
                    rel: 0,
                    enablejsapi: 1,
                    playsinline: 1,
                },
                events: {
                    onReady() {
                        if (timeToSeekTo > 0) {
                            vm.player.seekTo(timeToSeekTo);

                            let intervalTries = 0;

                            const pauseOnSeekInterval = setInterval(() => {
                                intervalTries += 1;

                                if (vm.player.getPlayerState() === 1) {
                                    vm.player.pauseVideo();
                                    window.clearInterval(pauseOnSeekInterval);
                                }

                                if (intervalTries > 100) {
                                    window.clearInterval(pauseOnSeekInterval);
                                }
                            }, 50);
                        }

                        vm.syncInterval = setInterval(() => {
                            vm.currentTime = vm.player.getCurrentTime();
                        }, 1000);
                    },
                    onStateChange(event) {
                        if (event.data === 1) {
                            vm.$emit('play', {
                                ...event,
                                contentId: vm.contentId,
                                progressState: vm.progressState,
                                isYoutube: true,
                            });
                        }

                        if (event.data === 2) {
                            vm.$emit('pause', {
                                ...event,
                                contentId: vm.contentId,
                            });
                        }
                    },
                },
            });
        },

        enableIntersectionObserver() {
            this.intersection = new IntersectionObserver((entries) => {
                entries.forEach((entry) => {
                    const isVisible = entry.intersectionRatio >= 0.5;
                    this.isPipEnabled = !isVisible && !this.isMobileViewport && this.isPlaying;
                });
            }, {
                root: null,
                rootMargin: '0px',
                threshold: 0.5,
            });
            this.intersection.observe(this.$refs.videoWrap.parentElement);
        },
    },
};
</script>
